public class Casting {
    //Casting in java
    //Implicit Casting is automatically done in java

    //Explicit casting
    //If we want to assign a value of larger datatype to a smaller datatype we perform explicit casting
    public static void main(String[] args) {


        short s = 32767;
        byte a = (byte) s;//Explicit Casting
        System.out.println(a);


    }




}
